// Tipos de Dato
// Un tipo de datos  en los lenguajes de programacion es una caracteristica que forma parte de las variables, esta caracteristica le obligara a solo poder almacenar un conjunto de valores compatibles.
// El tipo de dato es una clasificacion que toman las variables, de esta forma una variable que es del tipo numerica no puede almacenar una cadena de texto, de esta manera protegemos la informacion y tambien tenemos un orden en las operaciones que se pueden realizar con las variables dependiendo su tipo.
// En C# tenemos un conjunto de tipos de datos ya definidos, y hay tipos base que son llamados tipos de datos integrados, son tipos de datos nativos con los cuales vamos a almacenar informacion estandarizada, muchos de estos tipos los veras en otros lenguajes de programacion.
// Hay 2 tipos de integrados en C#:
// - Tipos de datos integrados de valor
// 	- bool, byte, sbyte, char, decimal, double, float, int, uint, nint, nuint, long, ulong, short, ushort
// - Tipos de datos integrados de referencia
// 	- object, string, dynamic
//
